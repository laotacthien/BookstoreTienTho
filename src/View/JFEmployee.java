    public void Viewtable(String name){
        DefaultTableModel model = (DefaultTableModel) this.tblEmployee.getModel();
        model.setNumRows(0);
        int n =1;
        for(Employee x : listE){         
            if(x.getTenNhanVien().contains(name)){
                model.addRow(new Object[]{n++, x.getIDNhanVien(), x.getTenNhanVien(), x.getTuoiNhanVien(), x.getViTriNhanVien(), x.getEmailNhanVien(), x.getDienThoaiNhanVien()});
            }
        }
    }
    
    public Employee Search(String s)
    {
        for (Employee x:listE)
        {
            if (x.getIDNhanVien().equals(s))
                return x;
        }
        return x;
    }
    
    public void ReadFile() {
        String idnhanvien, tennhanvien, vitrinhanvien, emailnhanvien, dienthoainhanvien;
        int tuoinhanvien;
        try {
            BufferedReader br = new BufferedReader(new FileReader("Employee.csv"));
            String line = br.readLine();
            while(line != null){
                String[] value = line.split(",");
                idnhanvien = value[0];
                tennhanvien = value[1];
                tuoinhanvien = Integer.parseInt(value[2]);
                vitrinhanvien = value[3];
                emailnhanvien = value[4];
                dienthoainhanvien = value[5];
                
                listE.add(new Employee(idnhanvien, tennhanvien, tuoinhanvien, vitrinhanvien, emailnhanvien, dienthoainhanvien));
                line = br.readLine();
            }
            br.close();
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
    }
    
    public void WriteFile(){
        try {
            BufferedWriter bw = new BufferedWriter(new FileWriter("Employee.csv"));
            for(Employee x : listE){
                bw.write(x.getIDNhanVien()+ "," + x.getTenNhanVien()+ "," + x.getTuoiNhanVien()+ ","
                + x.getViTriNhanVien()+ "," + x.getEmailNhanVien()+ "," + x.getDienThoaiNhanVien());
                bw.newLine();
            }
            bw.close();
        } catch (Exception e) {
            System.err.println(e.getMessage());
        }
    }
    
