    private void btnSaveNhanVienActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveNhanVienActionPerformed
        // TODO add your handling code here:
        String idNhanVien = this.txtIDNhanVien.getText();
        String tenNhanVien = this.txtTenNhanVien.getText();
        int tuoiNhanVien = Integer.parseInt(this.txtTuoiNhanVien.getText());
        String viTriNhanVien = (String) this.cbbViTriNhanVien.getSelectedItem();
        String emailNhanVien = this.txtEmailNhanVien.getText();
        String dienThoaiNhanVien = this.txtDienThoaiNhanVien.getText();
        //Kiểm tra điều kiện trước khi lưu
        
        if(check == 1){
            listE.add(new Employee(idNhanVien, tenNhanVien, tuoiNhanVien, viTriNhanVien, emailNhanVien, dienThoaiNhanVien));
        }
        if(check == -1){
            listE.set(pos, new Employee(idNhanVien, tenNhanVien, tuoiNhanVien, viTriNhanVien, emailNhanVien, dienThoaiNhanVien));
        }
        
        WriteFile();
        View();
        Viewtable(this.txtTimNhanVien.getText());
    }//GEN-LAST:event_btnSaveNhanVienActionPerformed

    private void btnDeleteNhanVienActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteNhanVienActionPerformed
        // TODO add your handling code here:
        UIManager.put("OptionPane.MessageFont", new FontUIResource(new Font("Arial", Font.BOLD, 20)));
        int n = JOptionPane.showConfirmDialog(panel,"Bạn có muốn xóa không", "Alert", JOptionPane.YES_NO_OPTION);
        if(n == JOptionPane.YES_NO_OPTION){
            listE.remove(pos);
            if(pos > listE.size() - 1) pos = pos - 1;
            if(pos < 0) pos = 0;
            
            WriteFile();
            View();
            Viewtable(this.txtTimNhanVien.getText());
        }
    }//GEN-LAST:event_btnDeleteNhanVienActionPerformed

